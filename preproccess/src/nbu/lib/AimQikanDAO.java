package nbu.lib;

public class AimQikanDAO {
	private int id;
	private String qikanname;
	private String issn;
	private String lingyu;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getQikanname() {
		return qikanname;
	}
	public void setQikanname(String qikanname) {
		this.qikanname = qikanname;
	}
	public String getIssn() {
		return issn;
	}
	public void setIssn(String issn) {
		this.issn = issn;
	}
	public String getLingyu() {
		return lingyu;
	}
	public void setLingyu(String lingyu) {
		this.lingyu = lingyu;
	}

}
